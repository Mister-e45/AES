void Cipher(Byte* block){
    setState(block);

    AddRoundKey(roundKeys);


    for(int i=1;i<Nr;++i){
        
        SubBytes();
        ShiftRows();
        MixColumns();
        AddRoundKey(roundKeys+i*16); //16 because a 128 bits block is made of 16 bytes

    }
    SubBytes();
    ShiftRows();
    AddRoundKey(roundKeys+Nr*16);
    
    outState(block);
    
}

void deCipher(Byte* block){
    setState(block);

    AddRoundKey(roundKeys+Nr*16);
    invShiftRows();
    invSubBytes();
    for(int i=1;i<Nr;++i){
        AddRoundKey(roundKeys+(Nr-i)*16);
        invMixColumns();
        invShiftRows();
        invSubBytes();
    }

    AddRoundKey(roundKeys);

    outState(block);
}

int readKey(char* k){//reads the key given as a string of hexadecimal values
    int i =0; //counts bytes (2 hexs characters)
    int count=0; // counts the characters in k allows to ignore spaces between hex pairs
    char* K=k;

    if(k[1]=='x') // if the key is written in 0x... format we ignore the first two characters
        K=k+2;
    while(K[count] != 0 && K[count+1] != 0 && i<32){ //32 is the maximum length in bytes of a key
        while(K[count]==' '){//ignore spaces between hex pairs
            ++count;
        }
        if(K[count]=='\0' || K[count+1]=='\0')//if no character after space exit with 0
            return 0;

        char bit[3]={K[count],K[count+1],'\0'};
        key[i]=(Byte)(strtol(bit,NULL,16)); // we convert the hex string to a long int then to a Byte (by truncation)
        count+=2; //we prepare to read the two next characters
        ++i; //we increment the byte count
    }
        
    if(i==16 || i==24 || i==32){
        keySize=i*8;
    
        return 1;
    }
    
    else{
        printf("invalid key size given \n");
        exit(0);
    }
        
}

