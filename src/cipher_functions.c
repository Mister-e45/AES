void SubWord(Byte* word){
    
    for(int i=0;i<Nb;++i)
        word[i]=SBox[ (unsigned int)(word[i]) ];
    
}

void RotWord(Byte* word){
    temp0=word[0];
    temp1=word[1];
    temp2=word[2];
    temp3=word[3];

    word[0]=temp1;
    word[1]=temp2;
    word[2]=temp3;
    word[3]=temp0;
}

void AddRoundKey(Byte* rKey){
    for(int j=0;j<Nb;++j)
        for(int i=0; i<Nb;++i){
            state[j][i]=state[j][i] ^ rKey[Nb*j+i];
        }
}

void takeKey(Byte* K,int size){ // generates the derived keys
    keySize=size;
    if(!keyIsLoaded){

        keyIsLoaded=1;
        
        

        int Nk=keySize/32;

        if(keySize==128){
            Nr=10;
        }
            
        else if(keySize==192){
            Nr=12;
        }
            
        else if(keySize==256){
            Nr=14;
        }
        else
            printf("Key size is not supported");
        
        Byte powX[17]={};
        powX[0]=0x0;
        Byte xPow=0x01;

        for(int i=1;i<=Nr;++i){
        powX[i]=xPow;
        xPow=xtime(xPow);
        }

        Byte temp[4]={};
        for(int i=0;i<Nk*4;++i){ // the first part is just the key
            roundKeys[i]=K[i];
        }
        

        for(int l=Nk;l<Nb*(Nr+1);++l){ // this loop generates the round keys 
            temp[0]=roundKeys[4*l-4];
            temp[1]=roundKeys[4*l-3];
            temp[2]=roundKeys[4*l-2];
            temp[3]=roundKeys[4*l-1];

            if(l%Nk==0){
                RotWord(temp);
                SubWord(temp);
                temp[0]= temp[0] ^ powX[l/Nk];

            }
            else if(Nk>6 && (l%Nk==4) ){
                SubWord(temp);
            }

            roundKeys[4*l]=  temp[0] ^ roundKeys[4*(l-Nk)];
            roundKeys[4*l+1]=temp[1] ^ roundKeys[4*(l-Nk)+1];
            roundKeys[4*l+2]=temp[2] ^ roundKeys[4*(l-Nk)+2];
            roundKeys[4*l+3]=temp[3] ^ roundKeys[4*(l-Nk)+3];
        }
        
    }

}

