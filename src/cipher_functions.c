void setState(Byte* s){
    for (int j =0;j<Nb;++j)
        for(int i =0;i<Nb;++i){
            state[j][i]=s[4*j+i];
        }
}

void outState(Byte* s){
    for (int j =0;j<Nb;++j){
        for(int i =0;i<Nb;++i){
            s[4*j+i]=state[j][i];
        }
    }
}

void SubBytes(){
    for(int j=0;j<Nb;++j)
        for(int i=0;i<Nb;++i){
            state[j][i]=SBox[ (unsigned int)(state[j][i])];
        }
}

void invSubBytes(){
    for(int j=0;j<Nb;++j)
        for(int i=0;i<Nb;++i){
            state[j][i]=invSBox[ (unsigned int)(state[j][i])];
        }
}

